class Rectangle {
  constructor(w, h) {
    this.w = w;
    this.h = h;
  }
  calcA() {
    return this.w * this.h;
  }
}

class Rectangle {
  constructor(width, height) {
    this.width = width;
    this.height = height;
  }
  calculateArea() {
    return this.width * this.height;
  }
}

